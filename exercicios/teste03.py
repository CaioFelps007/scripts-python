teste = input("Digite Algo: ")
print("O tipo primitivo é", type(teste))
print('Está em Alfabeto?', teste.isalpha())
print('Está em alfanumérico?', teste.isalnum())
print('Ele é printável?',teste.isprintable())
print('Esta em Maiúsculo?', teste.isupper())
print('Está em minúsculo?', teste.islower())
print('é decimal?', teste.isdecimal())



